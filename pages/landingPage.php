  <script>
    // Animate on scroll

    // Observer
    const observer = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('animate');
        } else {
          entry.target.classList.remove('animate');
        }
      });
    });

    const contentToAnimate = document.querySelectorAll('.content-to-animate');
    contentToAnimate.forEach((content) => observer.observe(content));

    // Add the 'animate' to all '.content-to-animate' class
    document.addEventListener('DOMContentLoaded', function() {
      contentToAnimate.forEach(content => {
        content.classList.add('animate');
      });
    });

    setTimeout(function() {
      contentToAnimate.forEach(content => {
        content.classList.remove('animate');
      });
    }, 1000);
  </script>
